package com.session1;

public class DataTypes {
    public static void main(String[] args) {
        byte b = 127; // byte -> 8 bits
        short s = 256; //short -> 2 bytes
        int i = 232323; // int -> 4 bytes
        long l = 2323243L; //long -> 8 bytes

        float f = 23.3434F; //float -> 4 bytes
        double d = 34.34343434d; //double -> 8 bytes

        char c = 'A'; //char -> 2 bytes
        char c1 = 65;
        System.out.println(c1);

        boolean b1 = false;//bool -> 1 byte
    }

}
