package com.session1;

public class ArithmeticOperators {
    public static void main(String[] args) {
        int a = 100, b = 10;
        int sum = a + b;
        System.out.println(sum);
        int sub = a - b;
        System.out.println(sub);
        int mul = a * b;
        System.out.println(mul);
        int quo = a/b;
        System.out.println(quo);
        int rem = a % b;
        System.out.println(rem);
    }
}
